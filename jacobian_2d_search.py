import sympy as sp
import sys

def solve_for_Q(P, x, y, Q_deg, c=1):
    """
    Given a polynomial P(x, y), finds a polynomial Q(x, y) of degree <= Q_deg
    such that Jac(P, Q) = P_x * Q_y - P_y * Q_x = c.
    Returns the general form of Q with free parameters, or None if no solution exists.
    """
    # Define unknown coefficients for Q
    coeffs = {}
    Q = 0
    for i in range(Q_deg + 1):
        for j in range(Q_deg + 1 - i):
            var_name = f"q_{i}_{j}"
            coef = sp.Symbol(var_name)
            coeffs[(i, j)] = coef
            Q += coef * (x**i) * (y**j)

    # Compute Jacobian
    P_x = sp.diff(P, x)
    P_y = sp.diff(P, y)
    Q_x = sp.diff(Q, x)
    Q_y = sp.diff(Q, y)

    Jac = (P_x * Q_y - P_y * Q_x).expand()

    # We want Jac = c
    # Collect coefficients of Jac with respect to x and y
    deg_x = sp.degree(Jac, x)
    deg_y = sp.degree(Jac, y)

    equations = []
    for ix in range(deg_x + 1):
        for iy in range(deg_y + 1):
            term_coef = Jac.coeff(x, ix).coeff(y, iy)
            # Remove any remaining x or y in term_coef just in case
            term_coef = term_coef.subs({x: 0, y: 0})
            if ix == 0 and iy == 0:
                equations.append(term_coef - c)
            else:
                if term_coef != 0:
                    equations.append(term_coef)

    # Solve the linear system
    sol = sp.solve(equations, list(coeffs.values()))

    if not sol:
        return None

    # Substitute solution back into Q
    Q_solved = Q.subs(sol)
    return Q_solved, sol

def check_injectivity_and_collisions(P, Q, x, y):
    """
    Checks if the map (P, Q) has any collisions.
    It does so by checking if there exist distinct points (x1, y1) and (x2, y2)
    such that P(x1, y1) = P(x2, y2) and Q(x1, y1) = Q(x2, y2).
    """
    x1, y1, x2, y2 = sp.symbols('x1 y1 x2 y2')
    P1 = P.subs({x: x1, y: y1})
    P2 = P.subs({x: x2, y: y2})
    Q1 = Q.subs({x: x1, y: y1})
    Q2 = Q.subs({x: x2, y: y2})

    eq1 = P1 - P2
    eq2 = Q1 - Q2

    # We want solutions where (x1, y1) != (x2, y2)
    # Solve system using Gröbner basis or solve
    # Since we want to find any non-trivial solution, let's analyze the ideal
    # generated by eq1, eq2 and a slack variable for (x1-x2)**2 + (y1-y2)**2 != 0
    t = sp.Symbol('t')
    eq3 = t * ((x1 - x2)**2 + (y1 - y2)**2) - 1

    # Try solving
    try:
        sol = sp.solve([eq1, eq2, eq3], (x1, y1, x2, y2, t), dict=True)
        if sol:
            collisions = []
            for s in sol:
                pt1 = (s[x1].evalf(), s[y1].evalf())
                pt2 = (s[x2].evalf(), s[y2].evalf())
                collisions.append((pt1, pt2))
            return False, collisions
    except Exception as e:
        return None, f"Error in solving: {e}"

    return True, []

