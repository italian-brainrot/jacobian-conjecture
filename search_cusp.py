    lam = 6 * sp.I * sp.sqrt(3)

    alpha = -sp.I / (2 * sp.sqrt(3))
    beta = sp.I / (2 * sp.sqrt(3))

    x1 = alpha * r + beta * s

    # Let's search for polynomial maps in r, s with constant Jacobian.
    # The three-point ideal is J = <r^3 - 8, rs - 4>.
    # Let's define some basis elements of the ideal with zero first jet or similar.
    # We want to find if there's any 2D map (F, G) in r, s such that
    # Jac(F, G) = 1 and F, G vanish on (2,2), (2*omega, 2*omega^2), (2*omega^2, 2*omega)
    # The basis for J can be generated by:
    # rs - 4, r^3 - 8, s^3 - 8, r^2*s - 4*r, etc.
    # Let's define the basis elements up to degree 3:
    basis = [
        r*s - 4,
        r**3 - 8,
        s**3 - 8,
        r*(r*s - 4),
        s*(r*s - 4),
        r**2*(r*s - 4),
        s**2*(r*s - 4),
        r*s*(r*s - 4)
    ]

    # Let's write a randomized modular search or a symbolic search
    # to see if a combination of these basis elements has constant Jacobian.
    print(f"Cusp search basis has {len(basis)} elements.")

